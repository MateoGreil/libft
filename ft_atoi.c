#include "libft.h"

int	ft_atoi(const char *str)
{
	int nb;
	int	i;

	while (*str && *str != '-' && *str != '+' && !(*str >= '0' && *str <= '1'))
		str++;
	i = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	nb = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (str[0] == '-')
		return (nb * -1);
	return (nb);
}
