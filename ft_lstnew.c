#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*new;

	new = NULL;
	if ((new = (t_list*)malloc(sizeof(t_list) * 1)) == NULL)
		return (NULL);
	if (content != NULL)
	{
		if ((new->content = (void*)malloc(content_size)) == NULL)
		{
			free(new);
			return (NULL);
		}
		memcpy(new->content, content, content_size);
		new->content_size = content_size;
	}
	else
	{
		new->content = NULL;
		new->content_size = 0;
	}
	new->next = NULL;
	return (new);
}
